    private void Ordena(String tipo, List<Queimadas> dados) {
        MetodosOrdenacao ordenador = new MetodosOrdenacao();

        switch (tipo) {
            case "BubbleSort":

                long startDesordenado = System.currentTimeMillis();
                ordenador.bubbleSort(dados, String.valueOf(jComboBox1.getSelectedItem()));
                long elapsed = System.currentTimeMillis() - startDesordenado;
                JOptionPane.showMessageDialog(null, "Bubble Sort: \n Demorou: " + elapsed + " ms");
                break;
            case "Selection Sort":
                long startDesordenado1 = System.currentTimeMillis();
                ordenador.selection(dados, String.valueOf(jComboBox1.getSelectedItem()));
                long elapsed2 = System.currentTimeMillis() - startDesordenado1;
                JOptionPane.showMessageDialog(null, "Selection Sort: \n Demorou: " +elapsed2 + "ms");
                break;
            case "Quick Sort":
                long startDesordenado3 = System.currentTimeMillis();
                ordenador.quick(dados, String.valueOf(jComboBox1.getSelectedItem()), 0, dados.size() - 1);
                long elapsed3 = System.currentTimeMillis() - startDesordenado3;
                JOptionPane.showMessageDialog(null, "Quick Sort:\n Demorou: " + elapsed3 + "ms");
                break;
                
        }
    }


