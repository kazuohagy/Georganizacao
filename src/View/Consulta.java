    private void montaTabelaCliente(List<Queimadas> dados) {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.getDataVector().removeAllElements();
        modelo.setNumRows(0);

        for (Queimadas q : dados) {
            modelo.addRow(new Object[]{
                q.getDatahora(),
                q.getSatelite(),
                q.getPais(),
                q.getEstado(),
                q.getMunicipio(),
                q.getBioma(),
                q.getDiasemchuva(),
                q.getPrecipitacao(),
                q.getRiscofogo(),
                q.getLatitude(),
                q.getLongitude(),
                q.getFrp(),
                q.getId()
            });
        }
    }

